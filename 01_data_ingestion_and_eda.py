# ── IMPORTS ──────────────────────────────────────
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import warnings
warnings.filterwarnings('ignore')

# Display settings
pd.set_option('display.max_columns', None)
pd.set_option('display.float_format', '{:.2f}'.format)
plt.style.use('seaborn-v0_8-whitegrid')
print("All libraries imported successfully!")

# ── STEP 1: Load Data ────────────────────────────
df = pd.read_csv('diabetes_binary_health_indicators_BRFSS2015.csv')

print("Shape:", df.shape)           # rows and columns
print("\nFirst 5 rows:")
df.head()

# ── STEP 2: Data Quality Check ───────────────────
print("Missing values:")
print(df.isnull().sum())

print("\nData types:")
print(df.dtypes)

print("\nBasic statistics:")
df.describe()

# ── STEP 3: Class Distribution ───────────────────
fig, axes = plt.subplots(1, 3, figsize=(15, 4))

targets = ['Diabetes_binary', 'HeartDiseaseorAttack', 'HighBP']
titles  = ['Diabetes', 'Heart Disease', 'High Blood Pressure']
colors  = ['#2E86C1', '#E74C3C', '#27AE60']

for i, (col, title, color) in enumerate(zip(targets, titles, colors)):
    counts = df[col].value_counts()
    axes[i].bar(['No Risk', 'At Risk'], counts.values, color=[color+'55', color])
    axes[i].set_title(f'{title}\nClass Distribution', fontweight='bold')
    axes[i].set_ylabel('Count')
    for j, v in enumerate(counts.values):
        axes[i].text(j, v + 500, f'{v:,}\n({v/len(df)*100:.1f}%)',
                    ha='center', fontsize=9)

plt.tight_layout()
plt.savefig('class_distribution.png', dpi=150, bbox_inches='tight')
plt.show()
print("Chart saved!")

# ── STEP 4: Correlation Heatmap ──────────────────
plt.figure(figsize=(14, 10))
corr = df.corr()
mask = np.triu(np.ones_like(corr, dtype=bool))
sns.heatmap(corr, mask=mask, annot=True, fmt='.2f',
            cmap='coolwarm', center=0,
            square=True, linewidths=0.5,
            cbar_kws={"shrink": 0.8})
plt.title('Feature Correlation Heatmap', fontsize=14, fontweight='bold', pad=15)
plt.tight_layout()
plt.savefig('correlation_heatmap.png', dpi=150, bbox_inches='tight')
plt.show()

# ── STEP 5: BMI vs Diabetes ──────────────────────
plt.figure(figsize=(10, 5))
df[df['Diabetes_binary']==0]['BMI'].hist(
    bins=40, alpha=0.6, color='#2E86C1', label='No Diabetes')
df[df['Diabetes_binary']==1]['BMI'].hist(
    bins=40, alpha=0.6, color='#E74C3C', label='Diabetes')
plt.xlabel('BMI', fontsize=12)
plt.ylabel('Count', fontsize=12)
plt.title('BMI Distribution by Diabetes Status', fontsize=14, fontweight='bold')
plt.legend(fontsize=11)
plt.tight_layout()
plt.savefig('bmi_distribution.png', dpi=150, bbox_inches='tight')
plt.show()